package EasyCalculator;

public class Functions {
    public double sum(double a, double b) {
        return a + b;
    }

    public double differenceNum(double a, double b) {
        return a - b;
    }

    public double multiply(double a, double b) {
        return a * b;
    }

    public Double attitude(double a, double b) {
        return a / b;
    }
}
